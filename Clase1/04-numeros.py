#VARIABLES DE TIPO INT "NUMEROS"

import math #Importar la libreria math

numero1 = 10.5
numero2 = 5

print(math.ceil(numero1)) #aproximacion de un numero flotante
print(math.floor(numero1)) #redondea un numero inferior
print(math.pow(numero2,2)) #elevar a una potencia
print(math.sqrt(numero2)) #raiz cuadrada